##WB22 STH Counting

#########################################################################################
####Data Setup###########################################################################
#########################################################################################

##Read in the Data
WBSTH <- read.csv("WB22_STH.csv")

attach(WBSTH)

View(WBSTH)

##########################################################################################
##########################################################################################
##########################################################################################


##########################################################################################
####Counting##############################################################################
##########################################################################################

##Initialize Values
RA = 0
RT = 0
RR = 0

SA = 0
ST = 0
SR = 0

##Count Values
for (i in 1:nrow(WBSTH)) {
  if (OrderType[i] == "R") {
    RA <- RA + 1
    RT <- RT + Tickets[i]
    RR <- RR + (Tickets[i] * PricePerTicket[i])
  }
  else if (OrderType[i] == "S") {
    SA <- SA + 1
    ST <- ST + Tickets[i]
    SR <- SR + (Tickets[i] * PricePerTicket[i])
  }
}
